    private void btnSolicitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSolicitarActionPerformed
        // TODO add your handling code here:
        if(txtClave.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "por favor ingrese la clave");
        } else {
            String clave = txtClave.getText();
            boolean respuesta = controlador.solicitarLlave(clave);
            if(respuesta){
                JOptionPane.showMessageDialog(null, "La clave es correcta, puede entregar la llave");
                limpiarCampos();
                VentanaPersonasCarpa ventana = new VentanaPersonasCarpa(carpa);
                ventana.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "la clave es incorrecta");
            }
        }
        
    }//GEN-LAST:event_btnSolicitarActionPerformed

